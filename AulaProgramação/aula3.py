# Variáveis:
'''Em programação, as variáveis são espaços na memória do computador que armazenam valores que podem ser modificados durante a execução de um programa. Elas são identificadas por nomes (identificadores) que permitem o acesso a esses valores. As variáveis são essenciais para armazenar e manipular dados, permitindo que os programas realizem operações complexas e dinâmicas. '''

# Tipos de Variáveis: 
''' Númericas: 
Inteiras (int): Armazenam números inteiros (ex:1,10,-5)
idade: int = 38

Ponto Flutuante (float): Armazenam números com casas decimais (ex:3.14, 2.5)
altura: float = 1.70

Textuais:
Caractere: Armazenam um único caractere (letra,número, símbolo)
blusa: caractere = "m"
idade: caractere = "38"

String (str): Armazenam sequências de caracteres (palavras, frases)
nome: str = "jãozinho"

Lógicas (Boolenas (bool)): Armazenam valores lógicos (verdadeiro ou falso), pode ser representado também por 1 ou 0.
sim: bool = True ou 1
não: bool = false ou 0

Outros:
Data: Armazenam datas e horários
Específicos de Linguagem: Algumas linguagens de programação têm tipos de variáveis específicos para determinados fins, como arrays, objetos, etc'''

#Operações Aritmáticas em Programação:
'''Em programação, as operações aritméticas básicas, são aquelas que realizam cálculos matemáticos simples com números. Elas incluem adição, subtração, multiplicação, divisão e, em alguns casos, exponenciação e resto da divisão (módulo)

PEMDAS - Significa uma regra, como fica as ordens dos operadores, (expressões).
 P - parenteses, E - exponenciação, M - multriplicação, D - divisão, A - adição, S - subtração
3 + (4 - 2) * 4 / 6 = 4.333
Sendo que quando segue esta regra o valor nao será o mesmo, caso não estiver nesta ordem.

Agora caso estiver sem parenteses, a regra não será respeitada, é sendo assim, o resultado não será o mesmo.
3 + 4 - 2 * 4 / 6 = 5.667 
'''

#Adição + (sinal usado para a Adição)
